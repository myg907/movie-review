////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import chalk from "chalk";

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export function logStart(message) {
    console.log(chalk.yellow(message)); console.log();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export function logError(error, message) {
    console.log(chalk.redBright(message)); console.log();
    console.error(error); console.log();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export function logFailure(message) {
    console.log(chalk.redBright(message)); console.log();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export function logSuccess(message) {
    console.log(chalk.greenBright(message)); console.log();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
